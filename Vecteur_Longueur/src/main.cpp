//===============================================
#include "GVec.h"
//===============================================
void length1() {
    GVec m_vec1(3, 4);
    double m_length1 = m_vec1.length();
    
    cout << "m_vec1 : " << m_vec1 << "\n";
    cout << "m_length1 : " << m_length1 << "\n";
}
//===============================================
void length2() {
    GVec m_vec1(3, 4, 5);
    double m_length1 = m_vec1.length();
    
    cout << "m_vec1 : " << m_vec1 << "\n";
    cout << "m_length1 : " << m_length1 << "\n";
}
//===============================================
int main(int argc, char** argv) {
    cout << "-------------------------------------------------\n";
    length1();
    cout << "-------------------------------------------------\n";
    length2();
    cout << "-------------------------------------------------\n";
    return 0;
}
