//===============================================
#include "GPoint.h"
//===============================================
void point1() {
    GPoint m_pt1(2, 4);
    GPoint m_pt2(5, 8);
    double m_dist = m_pt1.dist(m_pt2);
    
    cout << "m_pt1 : " << m_pt1 << "\n";
    cout << "m_pt2 : " << m_pt2 << "\n";
    cout << "m_dist : " << m_dist << "\n";
}
//===============================================
void point2() {
    GPoint m_pt1(2, 4, -1);
    GPoint m_pt2(5, 8, 4);
    double m_dist = m_pt1.dist(m_pt2);
    
    cout << "m_pt1 : " << m_pt1 << "\n";
    cout << "m_pt2 : " << m_pt2 << "\n";
    cout << "m_dist : " << m_dist << "\n";
}
//===============================================
int main(int argc, char** argv) {
    cout << "-------------------------------------------------\n";
    point1();
    cout << "-------------------------------------------------\n";
    point2();
    cout << "-------------------------------------------------\n";
    return 0;
}
